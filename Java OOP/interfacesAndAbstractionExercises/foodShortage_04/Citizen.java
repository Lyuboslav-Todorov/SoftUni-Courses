package interfacesAndAbstractionExercises.foodShortage_04;


public class Citizen implements Identifiable, Buyer {
    private String name;
    private int age;
    private String id;
    private String birthDate;
    private int food;

    public Citizen(String name, int age, String id, String birthDate) {
        this.name = name;
        this.age = age;
        this.id = id;
        this.birthDate = birthDate;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getId() {
        return id;
    }

    public int getFood() {
        return food;
    }

    public void buyFood() {
        food += 10;
    }
}
